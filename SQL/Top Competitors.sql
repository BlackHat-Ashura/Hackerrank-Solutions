SELECT H.HACKER_ID, H.NAME
FROM HACKERS H, CHALLENGES C, SUBMISSIONS S, DIFFICULTY D
WHERE
H.HACKER_ID = S.HACKER_ID AND
C.CHALLENGE_ID = S.CHALLENGE_ID AND
D.DIFFICULTY_LEVEL = C.DIFFICULTY_LEVEL AND
D.SCORE = S.SCORE
GROUP BY H.HACKER_ID, H.NAME HAVING COUNT(C.HACKER_ID) > 1
ORDER BY COUNT(C.CHALLENGE_ID) DESC, H.HACKER_ID ASC;
